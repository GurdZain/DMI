package com.ts.entity.nlp;

public class NlpData {
	
	private int NLPID;
	private String NLPNAME;
	private String NLPTYPE;
	private String AREAID;
	private String CLASSCODE;
	private String INTDATE;
	
	
	
	
	public String getCLASSCODE() {
		return CLASSCODE;
	}
	public void setCLASSCODE(String cLASSCODE) {
		CLASSCODE = cLASSCODE;
	}
	public String getAREAID() {
		return AREAID;
	}
	public void setAREAID(String aREAID) {
		AREAID = aREAID;
	}
	public int getNLPID() {
		return NLPID;
	}
	public void setNLPID(int nLPID) {
		NLPID = nLPID;
	}
	public String getNLPNAME() {
		return NLPNAME;
	}
	public void setNLPNAME(String nLPNAME) {
		NLPNAME = nLPNAME;
	}
	public String getNLPTYPE() {
		return NLPTYPE;
	}
	public void setNLPTYPE(String nLPTYPE) {
		NLPTYPE = nLPTYPE;
	}
	public String getINTDATE() {
		return INTDATE;
	}
	public void setINTDATE(String iNTDATE) {
		INTDATE = iNTDATE;
	}
	
	
	

}
